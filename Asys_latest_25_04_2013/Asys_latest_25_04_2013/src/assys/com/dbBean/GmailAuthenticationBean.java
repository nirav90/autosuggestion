public class GmailAuthenticationBean {
	
	private String empUserId,userName, gmailEmailId,gmailPassword;

	/**
	 * @return the empUserId
	 */
	public String getEmpUserId() {
		return empUserId;
	}

	/**
	 * @param empUserId the empUserId to set
	 */
	public void setEmpUserId(String empUserId) {
		this.empUserId = empUserId;
	}

	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}

	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}

	/**
	 * @return the gmailEmailId
	 */
	public String getGmailEmailId() {
		return gmailEmailId;
	}

	/**
	 * @param gmailEmailId the gmailEmailId to set
	 */
	public void setGmailEmailId(String gmailEmailId) {
		this.gmailEmailId = gmailEmailId;
	}

	/**
	 * @return the gmailPassword
	 */
	public String getGmailPassword() {
		return gmailPassword;
	}

	/**
	 * @param gmailPassword the gmailPassword to set
	 */
	public void setGmailPassword(String gmailPassword) {
		this.gmailPassword = gmailPassword;
	}

	
	
}
